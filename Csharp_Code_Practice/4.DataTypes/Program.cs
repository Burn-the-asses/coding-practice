using System;

namespace DataTypes
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("*****************Variable Data Types**************");
            int myNum = 5 ;
            double myDouble = 10.2f;
            char mycharacter = 'D';
            bool myBool = true;
            string myText = "Hello";
            long myNum1 = 150000000L;
            float myNum2 = 5.75F;

            System.Console.WriteLine(myNum);
            System.Console.WriteLine(myDouble);
            System.Console.WriteLine(mycharacter);
            System.Console.WriteLine(myBool);
            System.Console.WriteLine(myText);
            System.Console.WriteLine(myNum1);
            System.Console.WriteLine(myNum2);

        }
    }
}
